// for loop

console.log('1 to 10 all numbers :')
for (var i = 1; i <= 10; i++) {
  console.log(i)
}
// all the even numbers from 1 to 10

console.log('all the even numbers from 1 to 10:')
for (var i = 1; i <= 10; i++) {
  if (i % 2 == 0) {
    console.log(i)
  }
}
// all the odd numbers form 1 to 10
console.log('all the odd numbers from 1 to 10:')
for (var i = 1; i <= 10; i++) {
  if (i % 2 != 0) {
    console.log(i)
  }
}
